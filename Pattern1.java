class Pattern1 {
public static void main (String args[])
{
int i,j,k,l=0;
 for(i=10;i>=0;i--)
 {	for(k=0;k<l;k++)
	System.out.print("  ");
	l++; 
  for(j=0;j<=i;j++)
    {  System.out.print(" *");
    }

 System.out.println();
 }
}

}


